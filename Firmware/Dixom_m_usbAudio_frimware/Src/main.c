	TIM2->CCMR1 = temp;

	temp = TIM2->CCER;
	/* Select the Polarity and set the CC1E Bit */
	temp &= (uint32_t) ~(TIM_CCER_CC1P | TIM_CCER_CC1NP);
	temp |= (uint32_t) (TIM_CCER_CC1E);
	/* Write to TIM2 CCER register */
	TIM2->CCER = temp;

	/* Reset the IC1PSC Bits */
	TIM2->CCMR1 &= (uint32_t) ~TIM_CCMR1_IC1PSC;

	/* Set the Timer remapping configuration */
	TIM2->OR = (uint32_t) 0x0800;

	/* Get the TIM2 SMCR register value */
	temp = TIM2->SMCR;
	/* Reset the TS Bits */
	temp &= (uint32_t) ~TIM_SMCR_TS;
	/* Set the Input Trigger source */
	temp |= TIM_TS_ITR1;
	/* Write to TIM2 SMCR */
	TIM2->SMCR = temp;

