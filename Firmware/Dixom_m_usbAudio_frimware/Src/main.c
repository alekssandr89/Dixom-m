	if (HAL_TIM_Base_Init(&htim2) != HAL_OK) {
		Error_Handler();
	}

	temp = TIM2->SMCR;
	temp &= (uint32_t) 0x00ff;
	TIM2->SMCR = temp;
	TIM2->SMCR |= TIM_SMCR_ECE;

	/* Disable the Channel 1: Reset the CC1E Bit */
	TIM2->CCER &= (uint32_t) ~TIM_CCER_CC1E;

	temp = TIM2->CCMR1;
	/* Select the Input and set the filter */
	temp &= ((uint32_t) ~TIM_CCMR1_CC1S) & ((uint32_t) ~TIM_CCMR1_IC1F);
	temp |= (uint32_t) (TIM_CCMR1_CC1S);
